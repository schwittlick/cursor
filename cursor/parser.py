import pathlib
import typing
from typing import TextIO

import wasabi

from cursor.collection import Collection
from cursor.path import Path

log = wasabi.Printer()


class HPGLParser:
    IN = 'IN'
    SP = 'SP'
    PA = 'PA'
    LB = 'LB'
    SI = 'SI'

    def __init__(self, file: pathlib.Path):
        self.file = file

        self.pen_down = False
        self.drawn = False

        self.char_abs_width = 0
        self.char_abs_height = 0

        self.label_terminator = chr(3)

        self.paths = Collection()

        self.__init()

        self.cur_cr_x = self.cur_x
        self.cur_cr_y = self.cur_y

    def parse(self):
        with open(self.file.as_posix(), 'r', newline='') as glf:
            while True:
                c = self.__read(glf)
                while c in [';', ' ', '\r', '\n']:
                    c = self.__read(glf)
                cmd = c + self.__read(glf)
                cmd = cmd.upper()

                if cmd == HPGLParser.IN:
                    self.__init()
                elif cmd == HPGLParser.SP:
                    self.__parse_pen_select(glf)
                elif cmd == HPGLParser.PA:
                    self.__parse_pen_absolute(glf)
                elif cmd == HPGLParser.LB:
                    self.__parse_label(glf)
                elif cmd == HPGLParser.SI:
                    self.__parse_font_size(glf)

                if len(cmd) < 2:
                    break

        return self.paths

    def __init(self):
        self.pen_down = False
        self.cur_pen = 0
        self.cur_x = 0
        self.cur_y = 0
        self.cur_cr_x = self.cur_x
        self.cur_cr_y = self.cur_y

        self.paths.clear()

    def __read(self, f: TextIO) -> str:
        data = f.read(1)
        # log.info(data)
        return data

    def __parse_pen_select(self, glf: typing.TextIO):
        c = self.__read(glf)
        if c == ';':
            return
        self.cur_pen = int(c)

    def __parse_pen_absolute(self, glf: typing.TextIO):
        c = ''
        pts = [(self.cur_x, self.cur_y)]

        while c != ';':
            s = ''
            c = self.__read(glf)
            if c == ';':
                # switch to absolute plotting
                break
            while c == '-' or '0' <= c <= '9':
                s += c
                c = self.__read(glf)

            self.cur_x = int(s)

            s = ''
            c = self.__read(glf)
            while c == '-' or '0' <= c <= '9':
                s += c
                c = self.__read(glf)

            self.cur_y = int(s)

            self.cur_cr_x = self.cur_x
            self.cur_cr_y = self.cur_y

            pts.append((self.cur_x, self.cur_y))

        if self.pen_down:
            path = Path.from_tuple_list(pts)
            path.pen_select = self.cur_pen
            self.paths.add(path)
            self.drawn = True

    def __parse_label(self, glf: typing.TextIO):
        c = self.__read(glf)

        while c != self.label_terminator:
            if c == '\x08':
                self.cur_x -= self.char_abs_width * 3 / 2
            elif c == '\r':
                self.cur_x = self.cur_cr_x
                self.cur_y = self.cur_cr_y
            elif c == '\n':
                self.cur_cr_y -= self.char_abs_height * 2
                self.cur_x = self.cur_cr_x
                self.cur_y = self.cur_cr_y
            elif c < ' ':
                pass
            else:
                if c in stick_font:
                    chr_paths = stick_font[c]
                    for pts in chr_paths:
                        path = Path()
                        path.pen_select = self.cur_pen
                        for p in pts:
                            path.add(p[0] / 4 * self.char_abs_width + self.cur_x,
                                     p[1] / 8 * self.char_abs_width + self.cur_y)
                        self.paths.add(path)

                self.drawn = True
                self.cur_x += self.char_abs_width * 3 / 2
                if c == self.label_terminator:
                    break
            c = self.__read(glf)

    def __parse_font_size(self, glf: typing.TextIO):
        s = ''
        c = self.__read(glf)
        while c != ',':
            s += c
            c = self.__read(glf)
        self.char_abs_width = float(s)

        s = ''
        c = self.__read(glf)
        while c != ';':
            s += c
            c = self.__read(glf)
        self.char_abs_height = float(s)


